def is_barcode (id):
    if len(id)<12:
        return False
    if id[:4]!="TCGA":
        return False
    if id[4]!="-" and id[7]!="-":
        return False
    if string.split(id,"-") < 3:
        return False
    return True
    
def barcode_SampleType (barcode):
    #return the sample type code such as 01 (is primary tissue) and  20 (is cell line control)
    parts= string.split(barcode,"-")
    if len(parts)>=4:
        return parts[3][:2]
    else:
        return False

def barcode_IntegrationId (barcode):
    #return TCGA-01-1234
    parts= string.split(barcode,"-")
    if len(parts)<4:
        return None
    if len(parts[3])>2:
        parts[3]=parts[3][0:2]
    return string.join(parts[0:4],"-")
    
